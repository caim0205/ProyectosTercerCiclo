package modelo;

import java.util.Date;

/**
 *
 * @author santiago
 */
public class Historial {
    private Date fecha;
    private Venta venta;

    public Historial() {
    }

    public Historial(Date fecha, Venta venta) {
        this.fecha = fecha;
        this.venta = venta;
    }

    /**
     * @return the fecha
     */
    public Date getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    /**
     * @return the venta
     */
    public Venta getVenta() {
        return venta;
    }

    /**
     * @param venta the venta to set
     */
    public void setVenta(Venta venta) {
        this.venta = venta;
    }
    
    
}
